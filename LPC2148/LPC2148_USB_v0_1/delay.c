//---------------------------------------------------------------------
void delay_ms(int x)
{
	int a,b;
	for(a=0;a<x;a++){
		for(b=0;b<3500;b++);
	}
}
//---------------------------------------------------------------------
void delay_us(int x)
{
	int a,b;
	for(a=0;a<x;a++){
		for(b=0;b<4;b++);
}
}
